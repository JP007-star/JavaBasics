public class Arithmetic {
    public static void main(String[] args){
        int num1=23;
        int num2=34;
        int num3=45;

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3++);
        System.out.println(num3);
        System.out.println(++num3);

    }
}
